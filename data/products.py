def get_products_by_category(category):
    """Get all products for a specific category"""
    return PRODUCT_DATA.get(category, [])


def get_all_products():
    """Get all products across all categories"""
    all_products = []
    for category in PRODUCT_DATA:
        all_products.extend(PRODUCT_DATA[category])
    return all_products


def get_unique_product_names(category=None):
    """Get unique product names"""
    if category:
        products = get_products_by_category(category)
    else:
        products = get_all_products()
    
    return list(set([p['name'] for p in products]))